export function getCity(slug) {
  return cities.find((city) => city.slug === slug);
}

export function getVenue(citySlug, venueSlug) {
  const city = getCity(citySlug);
  if (!city) return null;
  return city.venues.find((venue) => venue.slug === venueSlug);
}

